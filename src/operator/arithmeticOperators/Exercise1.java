package operator.arithmeticOperators;

public class Exercise1 {
    public static void main (String [] args){

        int num1 = 3;
        int num2 = 4;

        int sum = (num1 + num2);
        int minus = (num2 - num1);
        int times = (num1 * num2);
        int div = (num2 / num1);
        int remainder = (num2 % num1);

        System.out.println("3+4 = " + sum);
        System.out.println("4-3 = " + minus);
        System.out.println("3*4 = " + times);
        System.out.println("4/3 = " + div);
        System.out.println("Remainder = " + remainder);

        System.out.println("Addition = " + (num1 + num2));
        System.out.println("Subtraction = " + (num2-num1));




    }
}
